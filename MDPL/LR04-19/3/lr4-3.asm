SD1	SEGMENT
	S1	DB	"1"
		DB	65533	DUP (0) ; 64 * 1024 - 2 - 1
SD1	ENDS

SD2	SEGMENT
	S2	DB	"2"
		DB	65533	DUP (?)
SD2	ENDS

SD3	SEGMENT
	S3	DB	"3"
		DB	65533	DUP (?)
SD3	ENDS

CSEG	SEGMENT
	ASSUME	CS:CSEG

MAIN:
	ASSUME	DS:SD1
		MOV	AX,SD1
		MOV	DS,AX
		MOV	DL,S1
		CALL	OUTPUT

	ASSUME	DS:SD2
		MOV	AX,SD2
		MOV	DS,AX
		MOV	DL,S2
		CALL	OUTPUT

	ASSUME	DS:SD3
		MOV	AX,SD3
		MOV	DS,AX
		MOV	DL,S3
		CALL	OUTPUT

		MOV	AX,4C00H
		INT	21H

OUTPUT	PROC	NEAR
		MOV	AH,2
		INT	21H
		MOV	DL,13
		INT	21H
		MOV	DL,10
		INT	21H
		RET
OUTPUT	ENDP

CSEG	ENDS

END	MAIN
