PUBLIC	SWAP
EXTRN	X:	BYTE

SD2	SEGMENT
	Y	DB	'Y'
SD2	ENDS

SC1	SEGMENT
	ASSUME	CS:SC1, DS:SD2

SWAP	PROC	FAR
		MOV	AX,SD2
		MOV	DS,AX
		MOV	AX,SEG	X
		MOV	ES,AX

		MOV	DL,Y
		MOV	DH,ES:X

		MOV	ES:X,DL
		MOV	Y,DH

		MOV	DL,Y
		MOV	AH,02H
		INT	21H

		MOV	AX,4C00H
		INT	21H
SWAP	ENDP

SC1	ENDS

END