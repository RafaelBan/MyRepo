PUBLIC	PROCESS
EXTRN	X: BYTE

VSEG	SEGMENT	AT 0B800H
	CA	LABEL	BYTE
	ORG	80*2 * 2 + 2*1
	Z	LABEL	WORD
VSEG	ENDS

CSEG	SEGMENT	PARA	PUBLIC	'CODE'
	ASSUME CS:CSEG, ES:VSEG

PROCESS PROC NEAR
	MOV	AX,VSEG
	MOV	ES,AX

	MOV	AL,'0'
	MOV	CA,AL

	MOV	AH,10
	MOV	AL,X
	MOV	Z,AX

	RET
PROCESS	ENDP

CSEG	ENDS

END